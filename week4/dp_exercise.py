# Importing the necessary libraries
import matplotlib.pyplot as plt
import numpy as np

def plot_2d_array(array, policy, goal_y=7, goal_x=10):
    """Takes in a 2D array of 0's and 1's and converts
    it to a plot of occupied and unoccupied spaces"""

    # Creating a figure and axes
    fig, ax = plt.subplots()
    # Set the size of the figure
    fig.set_size_inches(10, 20)
    # Creating a plot of the array
    ax.imshow(array, cmap = 'binary')
    # Color the goal state red
    ax.plot(goal_x, goal_y, 'ro')
    # In each unoccupied space, place a quiver
    for i in range(array.shape[0]):
        for j in range(array.shape[1]):
            if array[i][j] == 0:
                if policy[i][j] == 0:
                    ax.quiver(j, i, 0, -0.5)
                elif policy[i][j] == 1:
                    ax.quiver(j, i, 0.5, 0)
                elif policy[i][j] == 2:
                    ax.quiver(j, i, 0, 0.5)
                elif policy[i][j] == 3:
                    ax.quiver(j, i, -0.5, 0)
    # Displaying the plot
    plt.show()

